# 定义一个两个字符串相加的函数
def generate_full_name(firstname, lastname):
	return firstname + ' ' + lastname


# 定义一个两个数字相加的函数
def sum_two_numbers(num_one, num_two):
	return num_one + num_two


# 定义一个可以输出人的信息的函数
def person(name, age, gender):
	name = '姓名：' + name
	age = '年龄：' + 'age'
	gender = '性别：' + gender
	return name, age, gender
